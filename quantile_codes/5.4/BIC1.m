K=10000;
rho=0.2539;
T1=0;
T2=0;
for k=1:K
    W=normrnd(0,1,[2,962]);

    r1=-4.5886*10^(-5)+1.7148*10^(-2)*W(1,:)+1.7148*10^(-2)*sqrt(rho)*W(2,:);
    r2=-2.2799*10^(-5)+1.4129*10^(-2)*sqrt(rho)*W(1,:)+1.4129*10^(-2)*W(2,:);
    
    RSS1=sum((readata(2,:)-r1).^2);
    RSS2=sum((readata(1,:)-r2).^2);
    
    BIC1=962*log(RSS1/962)+3*log(962);
    BIC2=962*log(RSS2/962)+3*log(962);
    
    T1=T1+BIC1/K;
    T2=T2+BIC2/K;
end